nums = [10, 20, 30, 40]

print(max(nums)) # prints the maximum value in the list
print(min(nums)) # prints the minimum value in the list
print(sum(nums)) # prints the sum of all elements
print(sorted(nums)) # sorts in ascending order
print(sorted(nums, reverse=True)) # sorts in descending order
print(nums.count(20))
nums.insert(2, 25) # inserts 25 at index 2
print(nums)
nums.pop() # removes last element
print(nums)
nums.clear() # clears the list
print(nums)
